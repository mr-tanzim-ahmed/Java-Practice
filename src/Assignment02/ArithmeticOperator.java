package Assignment02;

public class ArithmeticOperator {
    public static void main(String[] args) {
        int a = 100;
        int b = 15;

        System.out.println("10 + 15 = " + (a + b));
        System.out.println("10 - 15 = " + (a - b));
        System.out.println("10 * 15 = " + (a * b));
        System.out.println("10 / 15 = " + (a / b));
        System.out.println("10 % 15 = " + (a % b));
    }
}
