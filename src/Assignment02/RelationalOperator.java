package Assignment02;

public class RelationalOperator {
    public static void main(String[] args) {
        int a = 25;
        int b = 20;
        System.out.println("a > b : " + (a>b));
        System.out.println("a < b : " + (a<b));

        System.out.println("a >= b : " + (a>=b));
        System.out.println("a <= b : " + (a<=b));

        System.out.println("a == b : " + (a==b));
        System.out.println("a != b : " + (a!=b));


    }
}
