package Assignment02;

public class UnaryOperator {
    public static void main(String[] args) {
        int a=20;
        int b=30;
        System.out.println("a=20 ,b=30");
        System.out.println("a++ = "+a++);
        System.out.println("a = "+a);
        System.out.println("++a = "+ ++a);
        System.out.println("a = "+a);

        System.out.println("--b = "+--b);
        System.out.println("b = "+b);
        System.out.println("b-- = "+b--);
        System.out.println("b = "+b);


    }
}
